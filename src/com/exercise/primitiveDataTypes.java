package com.exercise;

public class primitiveDataTypes {
    public static void main(String[] args) {
        int myNum=10;
        float myFloat=5.77f;
        char myChar='d';
        boolean myBool=true;
        System.out.println(myNum);
        System.out.println(myFloat);
        System.out.println(myChar);
        System.out.println(myBool);
    }
}
